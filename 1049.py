filo = input()
classe = input()
comida = input()

#Vertebrados
if (filo == "vertebrado"):
    if(classe == "ave"):
        if(comida == "carnivoro"):
            print("aguia")
        elif(comida == "onivoro"):
            print("pomba")
    elif(classe == "mamifero"):
        if(comida == "onivoro"):
            print ("homem")
        elif(comida == "herbivoro"):
            print("vaca");
                       
#Invertebrados
if (filo == "invertebrado"):
    if(classe == "inseto"):
        if(comida == "hematofago"):
            print("pulga")
        elif(comida == "herbivoro"):
            print("lagarta")
    elif(classe == "anelideo"):
        if(comida == "hematofago"):
            print("sanguessuga")
        elif(comida == "onivoro"):
            print("minhoca");
            
    
    
        

            
            

            
            
        
    
    

